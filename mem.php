					<p>There are two different models used to signify a memory location. The first is an energy field. This is the 
						primary indication that the user has encountered a memory location. The energy field is three dimensional 
						and will appear on screen hovering above a particular location or object. Tapping this energy field will activate 
						the memory location and cause the media attached to that memory, such as video or audio, to play.</p>

						<p><img class="img-responsive" src="assets/img/portfolio/mem3.jpg" alt=""></p>

					<p>The second 3D model is a large ‘i’. This is to signal that there is an information point. An information point is part of a 
						collection of detailed information regarding the event or location. It will often explain more about the memory than the media 
						contained in the energy field, and if there is image recognition available at that location, will also show what image is recognisable</p>


