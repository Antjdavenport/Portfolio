				<p>MemARy is a take on the word memory and augmented reality. It is designed to be a sort of interactive treasure hunt. By including 
					an element of gamification in this way, the project hopes to give the user some sense of motivation and reward in searching for 
					and finding the locations. The location of each historical information point are styled as memories. These are memories left behind, 
					not by one individual but of many people over various centuries. They have become interwoven into the location, be it a building or 
					3d space ready to be retrieved and examined though MemARy. Each memory is unique and regarding a number of different historical events 
